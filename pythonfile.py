# Ask the user for input
num = int(input("Enter a number: "))

# For loop to print the square of each number from 1 to num
print("\nSquares of numbers from 1 to", num)
for i in range(1, num + 1):
    print(f"{i} squared is {i**2}")

# While loop to count down from num to 1
print("\nCountdown from", num)
while num > 0:
    print(num)
    num -= 1

print("Done!")
